package JavaLoopsArray01;

public class ArrayLoops01 {
    public static void main(String[] args) {
        // Sum of numbers in the array

        int c[] = {2,5,8,7,4};
        int sum = 0;
        for (int i = 0; i <c.length ; i++) {
            // 5 times loop

            System.out.println(c[i]);
            sum = sum + c[i];

        }
        System.out.println(sum);

    }
}
